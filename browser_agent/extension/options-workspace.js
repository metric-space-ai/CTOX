function normalizeModuleNames(value) {
  const out = [];
  const seen = new Set();
  for (const entry of Array.isArray(value) ? value : [value]) {
    const text = asText(entry);
    if (!text || seen.has(text)) continue;
    seen.add(text);
    out.push(text);
  }
  return out;
}

function summarizeModuleNames(modules, maxVisible = 3) {
  const values = normalizeModuleNames(modules);
  if (!values.length) return "no target modules";
  if (values.length <= maxVisible) return values.join(" · ");
  const visible = values.slice(0, maxVisible).join(" · ");
  return `${visible} +${values.length - maxVisible}`;
}

function hasSerializedTransformerLora(adapter) {
  return Array.isArray(adapter?.modules) && adapter.modules.some(
    (entry) => asText(entry?.modulePath) && asText(entry?.loraADataUrl) && asText(entry?.loraBDataUrl),
  );
}

function inferTransformerLoraModules(adapter) {
  const out = [];
  const seen = new Set();
  for (const entry of Array.isArray(adapter?.modules) ? adapter.modules : []) {
    const modulePath = asText(entry?.modulePath);
    if (!modulePath) continue;
    const shortName = modulePath.split(".").at(-1) || modulePath;
    if (seen.has(shortName)) continue;
    seen.add(shortName);
    out.push(shortName);
  }
  return out;
}

function inferCurrentAdapterModules(weightsPayload) {
  const adapter = weightsPayload?.adapter && typeof weightsPayload.adapter === "object" ? weightsPayload.adapter : null;
  const explicitModules = normalizeModuleNames(adapter?.targetModules);
  if (explicitModules.length) return explicitModules;
  const transformerModules = inferTransformerLoraModules(adapter);
  if (transformerModules.length) return transformerModules;
  return [];
}

function buildModelArchitectureProfile(weightsPayload, craft = null) {
  const modelDescriptor = getWorkspaceModelDescriptor(weightsPayload, craft);
  const modelName = modelDescriptor.baseModelName;
  const adapter = weightsPayload?.adapter && typeof weightsPayload.adapter === "object" ? weightsPayload.adapter : null;
  const qwen35SizeLabel = getQwen35SizeLabel(modelName);
  const isQwen35 = Boolean(qwen35SizeLabel);
  const architectureSpec = getQwen35ArchitectureSpec(qwen35SizeLabel);
  const layerTypes = Array.isArray(architectureSpec?.layerTypes) ? architectureSpec.layerTypes : [];
  const linearLayerCount = layerTypes.filter((layerType) => layerType === "linear").length;
  const fullLayerCount = layerTypes.filter((layerType) => layerType === "full").length;
  const attentionTargetCount = architectureSpec
    ? (linearLayerCount * architectureSpec.linearTargets.length) + (fullLayerCount * architectureSpec.fullTargets.length)
    : 0;
  const mlpTargetCount = architectureSpec ? architectureSpec.numLayers * architectureSpec.mlpTargets.length : 0;
  const totalTargetCount = attentionTargetCount + mlpTargetCount;
  const structureSummary = architectureSpec
    ? `${architectureSpec.visionDepth} vision blocks · ${architectureSpec.numLayers} decoder blocks · ${linearLayerCount} linear-attention · ${fullLayerCount} full-attention · ${totalTargetCount} LoRA-enabled MatMul targets`
    : "";
  const architecture = isQwen35
    ? {
        title: `Qwen3.5 ${qwen35SizeLabel}`,
        runtimeTitle: trimText(modelDescriptor.runtimeModelId || "Local ONNX runtime", 28),
        runtimeMeta: modelDescriptor.runtimeMeta || "WebGPU q4 / fp16",
        outputMeta: "next-token logits",
      }
    : null;
  const hasSerializableLora = hasSerializedTransformerLora(adapter);
  const currentModules = hasSerializableLora ? inferCurrentAdapterModules(weightsPayload) : [];
  const hasAdapter = hasSerializableLora && currentModules.length > 0;
  const currentModuleLabel = summarizeModuleNames(currentModules);
  const summary = hasAdapter
    ? [structureSummary, `Vision tower stays frozen; attached transformer LoRA on ${currentModuleLabel}.`].filter(Boolean).join(" · ")
    : [structureSummary, "Vision tower and text embeddings feed the decoder below, but no adapter weights are attached."].filter(Boolean).join(" · ");
  const statusLabel = hasAdapter
    ? `Attached LoRA on ${currentModuleLabel}`
    : "No attached adapter";

  return {
    title: architecture?.title || trimText(modelName, 48) || "Model",
    summary,
    statusLabel,
    runtimeTitle: architecture?.runtimeTitle || "Base runtime",
    runtimeMeta: architecture?.runtimeMeta || "frozen forward pass",
    patchTitle: hasAdapter ? "Attached LoRA" : "",
    patchMeta: hasAdapter ? currentModuleLabel : "",
    patchMode: hasAdapter ? "learned low-rank weights" : "frozen_only",
    outputTitle: "Output logits",
    outputMeta: architecture?.outputMeta || "next-token logits",
    activeModules: currentModules,
    moduleAriaLabel: hasAdapter ? currentModules.join(", ") : "no attached LoRA modules",
    emptyStateTitle: "No adapter attached",
    emptyStateMeta: "frozen backbone only",
    architectureSpec,
    linearLayerCount,
    fullLayerCount,
    attentionTargetCount,
    mlpTargetCount,
    totalTargetCount,
    hasAdapter,
  };
}

