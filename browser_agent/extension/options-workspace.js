function getCraftSessionNote(craft, syncSnapshot, syncSettings) {
  const session = `${formatCount(getLiveLinkCount(syncSnapshot))} live · ${formatCount(
    getVisiblePeerCount(syncSnapshot),
  )} peers`;
  if (isRemoteSharedCraft(craft)) {
    return `Read only · ${session}`;
  }
  if (craft?.sharing?.enabled === true) {
    if (!hasCustomSyncCode(syncSettings)) {
      return `Share code missing · ${session}`;
    }
    return `Shared · ${session}`;
  }
  return hasCustomSyncCode(syncSettings)
    ? `Local only · ${session}`
    : `Private code · ${session}`;
}

