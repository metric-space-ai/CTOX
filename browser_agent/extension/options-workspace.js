async function readArtifactRecord(artifactId, fallback = null, options = {}) {
  const key = asText(artifactId);
  const craftId = asText(options?.craftId);
  const kind = asText(options?.kind);
  const craft = options?.craft || null;
  const allowEmbeddedFallback = shouldUseEmbeddedWorkspaceBundleFallback(craft);
  if (!key) {
    return allowEmbeddedFallback ? findEmbeddedBundleArtifactRecord(craft, "", kind) || fallback : fallback;
  }
  const craftSync = getCraftSyncApi();
  if (craftSync?.readLocalArtifact) {
    try {
      const record = await craftSync.readLocalArtifact(key, null);
      if (record) return record;
    } catch (_error) {}
  }
  if (craftId && craftSync?.listLocalArtifacts) {
    const records = await listLocalArtifactRecords(craftId, kind);
    const matchedRecord =
      records.find((record) => asText(record?.id) === key) ||
      (kind ? records.find((record) => asText(record?.kind) === kind) : null) ||
      null;
    if (matchedRecord) return matchedRecord;
  }
  return allowEmbeddedFallback ? findEmbeddedBundleArtifactRecord(craft, key, kind) || fallback : fallback;
}

