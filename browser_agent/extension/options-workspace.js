function normalizeCapabilityLookupKey(value) {
  return asText(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
}

function findToolScriptForCapability(capability, toolScriptsPayload) {
  const scripts = Array.isArray(toolScriptsPayload?.scripts) ? toolScriptsPayload.scripts : [];
  const keys = new Set(
    [capability?.id, capability?.name]
      .map((entry) => normalizeCapabilityLookupKey(entry))
      .filter(Boolean),
  );
  return (
    scripts.find((script) => {
      const scriptKeys = [script?.id, script?.name]
        .map((entry) => normalizeCapabilityLookupKey(entry))
        .filter(Boolean);
      return scriptKeys.some((entry) => keys.has(entry));
    }) || null
  );
}

