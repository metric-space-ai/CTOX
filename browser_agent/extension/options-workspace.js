function buildSvgArrow(keyPrefix, x1, y1, x2, y2, className = "workspace-model-svg-arrow-line") {
  const angle = Math.atan2(y2 - y1, x2 - x1);
  const size = 10;
  const wing = 5;
  const backX = x2 - Math.cos(angle) * size;
  const backY = y2 - Math.sin(angle) * size;
  const leftX = backX + Math.cos(angle + Math.PI / 2) * wing;
  const leftY = backY + Math.sin(angle + Math.PI / 2) * wing;
  const rightX = backX + Math.cos(angle - Math.PI / 2) * wing;
  const rightY = backY + Math.sin(angle - Math.PI / 2) * wing;
  return [
    h("line", { x1, y1, x2, y2, className, key: `${keyPrefix}:line` }),
    h(
      "polygon",
      {
        points: `${x2},${y2} ${leftX},${leftY} ${rightX},${rightY}`,
        className: className.replace("line", "head"),
        key: `${keyPrefix}:head`,
      },
    ),
  ];
}

