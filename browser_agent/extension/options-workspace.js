function buildWorkspaceArtifactEntries(artifactState) {
  const data = artifactState?.trainingData || {};
  const toolScripts = artifactState?.toolScripts || {};
  const browser = artifactState?.browserCapabilities || {};
  const policy = artifactState?.policy || {};
  const weights = artifactState?.weights || {};
  const browserValue = !browser.present
    ? "Missing"
    : browser.runtimeOk
      ? "Published"
      : asText(browser.publicationStatus).toLowerCase() === "published"
        ? "Invalid"
        : "Draft only";

  return [
    {
      label: "Data artifact",
      value: data.present ? `${formatCount(data.sampleCount)} rows` : "Empty",
      meta: [
        data.readyCount ? `${formatCount(data.readyCount)} ready` : "",
        data.updatedAt ? `saved ${formatTimestamp(data.updatedAt)}` : "",
      ]
        .filter(Boolean)
        .join(" · "),
    },
    {
      label: "Tool scripts",
      value: toolScripts.present ? `${formatCount(toolScripts.scriptCount)} scripts` : "Missing",
      meta: [
        toolScripts.declaredToolCount ? `${formatCount(toolScripts.declaredToolCount)} declared tools` : "",
        toolScripts.updatedAt ? `saved ${formatTimestamp(toolScripts.updatedAt)}` : "",
      ]
        .filter(Boolean)
        .join(" · "),
    },
    {
      label: "Browser runtime",
      value: browserValue,
      meta: [
        browser.capabilityCount ? `${formatCount(browser.capabilityCount)} functions` : "",
        browser.runtimePackageVersion ? `runtime v${browser.runtimePackageVersion}` : "",
        browser.publishedBy ? `by ${browser.publishedBy}` : "",
        browser.runtimeOk ? "" : trimText(browser.runtimeError, 88),
      ]
        .filter(Boolean)
        .join(" · "),
    },
    {
      label: "Policy",
      value: policy.present ? "Stored" : "Missing",
      meta: [
        policy.allowedToolCount ? `${formatCount(policy.allowedToolCount)} allowed tools` : "",
        policy.completionSignalCount ? `${formatCount(policy.completionSignalCount)} completion signals` : "",
        policy.updatedAt ? `saved ${formatTimestamp(policy.updatedAt)}` : "",
      ]
        .filter(Boolean)
        .join(" · "),
    },
    {
      label: "Model artifact",
      value: weights.present ? (weights.adapterAttached ? "Adapter attached" : humanizeArtifactLabel(weights.status, "Stored")) : "Not trained",
      meta: [
        trimText(weights.baseModelName, 48),
        weights.runtimeModelId ? trimText(weights.runtimeModelId, 56) : "",
        weights.updatedAt ? `saved ${formatTimestamp(weights.updatedAt)}` : "",
      ]
        .filter(Boolean)
        .join(" · "),
    },
  ];
}

