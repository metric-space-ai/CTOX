function getQwen35SizeLabel(modelName = "") {
  const match = /qwen3\.5-(0\.8b|2b|4b)/i.exec(asText(modelName));
  return match ? match[1].toUpperCase() : "";
}

function getQwen35ArchitectureSpec(sizeLabel = "") {
  const normalized = asText(sizeLabel).toUpperCase();
  const spec = QWEN35_ARCHITECTURE_SPECS[normalized];
  if (!spec) return null;
  const fullLayerSet = new Set(spec.fullAttentionLayers);
  return {
    ...spec,
    layerTypes: Array.from({ length: spec.numLayers }, (_, layerId) =>
      fullLayerSet.has(layerId) ? "full" : "linear"
    ),
  };
}

