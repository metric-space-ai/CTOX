function scoreTrainingDataArtifactRecord(record) {
  const samples = Array.isArray(record?.payload?.samples) ? record.payload.samples : [];
  return samples.reduce((score, sample) => {
    const messageCount = Array.isArray(sample?.messages) ? sample.messages.length : 0;
    const targetTurnIndex = normalizeTrainingTargetTurnIndex(sample?.target_turn_index ?? sample?.targetTurnIndex);
    return (
      score +
      100 +
      messageCount * 5 +
      (Number.isInteger(targetTurnIndex) ? 20 : 0) +
      (asText(sample?.id) ? 2 : 0)
    );
  }, 0);
}

