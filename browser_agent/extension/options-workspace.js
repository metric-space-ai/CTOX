function summarizeExecutionDtype(executionPlan = null) {
  const dtype = executionPlan?.dtype;
  if (!dtype || typeof dtype !== "object") return "";
  return Object.entries(dtype)
    .map(([key, value]) => `${key.replace(/_/g, " ")} ${String(value || "").trim()}`.trim())
    .filter(Boolean)
    .join(" · ");
}

function resolveLocalQwenRuntimeDescriptor(modelName = "", runtime = null) {
  const candidates = [
    asText(modelName),
    asText(runtime?.runtimeModelId),
    asText(runtime?.remoteRuntimeModelId),
  ].filter(Boolean);
  let runtimePlan = null;

  for (const candidate of candidates) {
    try {
      runtimePlan = getLocalQwenRuntimePlan(candidate);
      break;
    } catch (_error) {
      runtimePlan = null;
    }
  }

  const executionPlan =
    runtime?.executionPlan && typeof runtime.executionPlan === "object"
      ? runtime.executionPlan
      : runtimePlan?.executionPlans?.[0] || null;

  return {
    runtimeModelId: asText(runtime?.runtimeModelId || runtimePlan?.runtimeModelId),
    executionLabel: asText(executionPlan?.label || runtimePlan?.runtimeDtypeSummary),
    executionDtype: summarizeExecutionDtype(executionPlan),
    requestedSizeLabel: asText(runtimePlan?.requestedSizeLabel),
  };
}

function getWorkspaceModelDescriptor(weightsPayload, craft = null) {
  const sourceModelName = asText(weightsPayload?.modelName || craft?.starterModelName || "base model");
  const runtimeDescriptor = resolveLocalQwenRuntimeDescriptor(sourceModelName, {
    ...(weightsPayload?.runtime && typeof weightsPayload.runtime === "object" ? weightsPayload.runtime : {}),
    runtimeModelId: asText(weightsPayload?.runtime?.runtimeModelId || weightsPayload?.runtimeModelId),
    remoteRuntimeModelId: asText(weightsPayload?.runtime?.remoteRuntimeModelId || weightsPayload?.remoteRuntimeModelId),
  });
  const displayBaseModelName = runtimeDescriptor.requestedSizeLabel
    ? `Qwen/Qwen3.5-${runtimeDescriptor.requestedSizeLabel}`
    : sourceModelName;
  return {
    baseModelName: displayBaseModelName,
    sourceModelName,
    runtimeModelId: runtimeDescriptor.runtimeModelId,
    runtimeMeta: [runtimeDescriptor.executionLabel, runtimeDescriptor.executionDtype]
      .filter(Boolean)
      .join(" · "),
  };
}

