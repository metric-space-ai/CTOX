function useCraftCatalog({ includeSyncSettings = false } = {}) {
  const mountedRef = React.useRef(true);
  const [state, setState] = React.useState({
    loading: true,
    error: "",
    crafts: [],
    localCrafts: [],
    syncSettings: DEFAULT_SYNC_SETTINGS,
    syncSnapshot: getCraftSyncApi()?.getState?.() || null,
  });

  const load = React.useCallback(async () => {
    try {
      const craftStore = getCraftStoreApi();
      const craftSync = getCraftSyncApi();
      const [nextLocalCrafts, nextCrafts, nextSyncSettings] = await Promise.all([
        craftStore?.readLocalCrafts?.() || [],
        craftStore?.readCrafts?.() || [],
        includeSyncSettings ? craftSync?.readSettings?.() || DEFAULT_SYNC_SETTINGS : DEFAULT_SYNC_SETTINGS,
      ]);
      if (!mountedRef.current) return;
      setState({
        loading: false,
        error: "",
        crafts: Array.isArray(nextCrafts) ? nextCrafts : [],
        localCrafts: Array.isArray(nextLocalCrafts) ? nextLocalCrafts : [],
        syncSettings: normalizeSyncSettings(nextSyncSettings),
        syncSnapshot: craftSync?.getState?.() || null,
      });
    } catch (error) {
      if (!mountedRef.current) return;
      setState((previous) => ({
        ...previous,
        loading: false,
        error: error instanceof Error ? error.message : String(error || "Workspace data could not be loaded."),
        syncSnapshot: getCraftSyncApi()?.getState?.() || previous.syncSnapshot,
      }));
    }
  }, [includeSyncSettings]);

  const handleSyncEvent = React.useCallback((payload) => {
    const type = asText(payload?.detail?.type);
    if (!type) {
      void load();
      return;
    }

    const shouldReload = [
      "settings-updated",
      "kv-upsert",
      "kv-delete",
      "local-crafts",
      "remote-crafts",
      "local-artifact-upsert",
      "local-artifact-delete",
      "publish-local-crafts",
    ].includes(type);

    if (!shouldReload) {
      setState((previous) => ({
        ...previous,
        syncSnapshot: getCraftSyncApi()?.getState?.() || previous.syncSnapshot,
      }));
      return;
    }

    void load();
  }, [load]);

  React.useEffect(() => {
    mountedRef.current = true;
    void load();
    const craftSync = getCraftSyncApi();
    const unsubscribe = craftSync?.subscribe
      ? craftSync.subscribe((payload) => {
          if (!mountedRef.current) return;
          handleSyncEvent(payload);
        })
      : null;
    return () => {
      mountedRef.current = false;
      unsubscribe?.();
    };
  }, [handleSyncEvent, load]);

  return {
    ...state,
    reload: load,
  };
}

function isTrainingArtifactEvent(detail, craftId) {
  if (isLocalArtifactMutationForCraft(detail, craftId, [TRAINING_DATA_ARTIFACT_KIND])) {
    const type = asText(detail?.type);
    const artifactId = asText(detail?.artifactId);
    return (
      type === "local-artifacts" ||
      asText(detail?.kind) === TRAINING_DATA_ARTIFACT_KIND ||
      artifactId === getTrainingDataArtifactId(craftId)
    );
  }
  return false;
}

function isLocalArtifactMutationForCraft(detail, craftId, acceptedKinds = []) {
  const artifactCraftId = asText(detail?.craftId);
  const normalizedCraftId = asText(craftId);
  const type = asText(detail?.type);
  const kind = asText(detail?.kind);
  if (!normalizedCraftId || !type) return false;
  if (type === "local-artifacts") return true;
  if (!["local-artifact-upsert", "local-artifact-delete"].includes(type)) return false;
  if (artifactCraftId && artifactCraftId !== normalizedCraftId) return false;
  if (!acceptedKinds.length || !kind) return true;
  return acceptedKinds.includes(kind);
}

function useTrainingDataState(craft, onSaved = null) {
  const craftId = asText(craft?.id);
  const editable = isCraftEditable(craft);
  const dirtyRef = React.useRef(false);
  const savingRef = React.useRef(false);
  const [state, setState] = React.useState({
    loading: Boolean(craftId),
    saving: false,
    saveError: "",
    artifactId: craftId ? getTrainingDataArtifactId(craftId) : "",
    samples: [],
    lastSavedAt: "",
    dirty: false,
  });

  React.useEffect(() => {
    dirtyRef.current = state.dirty;
    savingRef.current = state.saving;
  }, [state.dirty, state.saving]);

  const load = React.useCallback(async () => {
    if (!craftId) {
      setState({
        loading: false,
        saving: false,
        saveError: "",
        artifactId: "",
        samples: [],
        lastSavedAt: "",
        dirty: false,
      });
      return;
    }

    setState((previous) => ({
      ...previous,
      loading: true,
      saveError: "",
      artifactId: getTrainingDataArtifactId(craftId),
    }));

    try {
      const artifact = (await readTrainingDataArtifactRecordWithRetry(craft)) || {
        id: getTrainingDataArtifactId(craftId),
        payload: { samples: [] },
      };
      const samples = Array.isArray(artifact?.payload?.samples)
        ? artifact.payload.samples.map((sample, index) => normalizeTrainingSample(sample, index))
        : [];
      setState({
        loading: false,
        saving: false,
        saveError: "",
        artifactId: asText(artifact?.id || getTrainingDataArtifactId(craftId)),
        samples,
        lastSavedAt: resolveRecordUpdatedAt(artifact, ""),
        dirty: false,
      });
    } catch (error) {
      setState((previous) => ({
        ...previous,
        loading: false,
        saveError:
          error instanceof Error ? error.message : String(error || "Training data could not be loaded."),
      }));
    }
  }, [craft, craftId]);

  React.useEffect(() => {
    void load();
  }, [load]);

  React.useEffect(() => {
    if (!craftId) return undefined;
    const craftSync = getCraftSyncApi();
    const unsubscribe = craftSync?.subscribe
      ? craftSync.subscribe((payload) => {
          const detail = payload?.detail || {};
          const type = asText(detail?.type);
          if (!type) {
            if (dirtyRef.current || savingRef.current) return;
            void load();
            return;
          }
          if (!isTrainingArtifactEvent(detail, craftId)) return;
          if (dirtyRef.current || savingRef.current) return;
          void load();
        })
      : null;
    return () => {
      unsubscribe?.();
    };
  }, [craftId, load]);

  const persistSamples = React.useCallback(
    async (samplesToPersist) => {
      if (!craftId || !editable) return;
      setState((previous) => ({
        ...previous,
        saving: true,
        saveError: "",
      }));
    try {
      const normalizedSamples = (Array.isArray(samplesToPersist) ? samplesToPersist : []).map((sample, index) =>
        normalizeTrainingSample(sample, index),
      );
      for (const sample of normalizedSamples) {
        ensureNativeQwenTrainingSample(sample);
      }
      const record = await writeTrainingDataArtifactRecord(craftId, serializeTrainingSamples(normalizedSamples));
        await syncCraftTrainingDataSummary(craftId, normalizedSamples);
        if (typeof onSaved === "function") {
          await onSaved();
        }
        setState((previous) => ({
          ...previous,
          saving: false,
          saveError: "",
          samples: normalizedSamples,
          dirty: false,
          artifactId: asText(record?.id || previous.artifactId || getTrainingDataArtifactId(craftId)),
          lastSavedAt: resolveRecordUpdatedAt(record, new Date().toISOString()),
        }));
      } catch (error) {
        setState((previous) => ({
          ...previous,
          saving: false,
          dirty: false,
          saveError:
            error instanceof Error ? error.message : String(error || "Training data could not be saved."),
        }));
      }
    },
    [craftId, editable, onSaved],
  );

  React.useEffect(() => {
    if (!craftId || !state.dirty || state.saving || !editable) return undefined;
    const timer = globalThis.setTimeout(() => {
      void persistSamples(state.samples);
    }, 180);
    return () => {
      globalThis.clearTimeout(timer);
    };
  }, [craftId, editable, persistSamples, state.dirty, state.samples, state.saving]);

  const updateSample = React.useCallback(
    (sampleId, field, value) => {
      if (!editable) return;
      setState((previous) => ({
        ...previous,
        saveError: "",
        dirty: true,
        samples: previous.samples.map((sample) => {
          if (sample.id !== sampleId) return sample;
          const nextValue =
            field === "split"
              ? normalizeTrainingSampleSplit(value)
              : field === "status"
                ? normalizeTrainingSampleStatus(value)
                : String(value || "");
          return {
            ...sample,
            [field]: nextValue,
            updatedAt: new Date().toISOString(),
          };
        }),
      }));
    },
    [editable],
  );

  const addSample = React.useCallback(() => {
    if (!editable) return;
    setState((previous) => ({
      ...previous,
      saveError: "",
      dirty: true,
      samples: [createTrainingSampleDraft(previous.samples.length), ...previous.samples],
    }));
  }, [editable]);

  const deleteSample = React.useCallback(
    (sampleId) => {
      if (!editable) return;
      setState((previous) => ({
        ...previous,
        saveError: "",
        dirty: true,
        samples: previous.samples.filter((sample) => sample.id !== sampleId),
      }));
    },
    [editable],
  );

  return {
    ...state,
    editable,
    meta: getTrainingDataMeta(state.samples),
    updateSample,
    addSample,
    deleteSample,
    reload: load,
  };
}

