async function ensureCraftSyncLoaded() {
  const existing = getCraftSync();
  if (existing) {
    craftSyncLoadError = "";
    return existing;
  }
  if (craftSyncLoadPromise) return craftSyncLoadPromise;

  craftSyncLoadPromise = import("./shared/craft-sync.js")
    .then(() => {
      const loaded = getCraftSync();
      if (!loaded) {
        throw new Error("Craft sync runtime did not register on globalThis.");
      }
      craftSyncLoadError = "";
      return loaded;
    })
    .catch((error) => {
      craftSync = null;
      craftSyncLoadError =
        error instanceof Error ? error.message : String(error || "Craft sync could not be loaded.");
      console.warn("[options] craft sync bootstrap failed", error);
      return null;
    })
    .finally(() => {
      craftSyncLoadPromise = null;
    });

  return craftSyncLoadPromise;
}

