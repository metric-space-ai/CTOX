function usePageRoute() {
  const [route, setRoute] = React.useState(readPageRoute());

  React.useEffect(() => {
    const handleLocationChange = () => {
      setRoute(readPageRoute());
    };
    window.addEventListener("hashchange", handleLocationChange);
    window.addEventListener("popstate", handleLocationChange);
    return () => {
      window.removeEventListener("hashchange", handleLocationChange);
      window.removeEventListener("popstate", handleLocationChange);
    };
  }, []);

  const updateRoute = React.useCallback((patch = {}) => {
    const current = readPageRoute();
    const nextRoute = {
      ...current,
      ...patch,
    };
    nextRoute.section = normalizeSectionId(nextRoute.section) || "general";
    writePageRoute(nextRoute);
    setRoute(readPageRoute());
  }, []);

  return [route, updateRoute];
}

