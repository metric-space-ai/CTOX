function buildSyncStatusLine(snapshot, settings = DEFAULT_SYNC_SETTINGS, localCrafts = []) {
  const syncState = snapshot?.sync || snapshot || null;
  const passwordNote = hasCustomSyncCode(settings)
    ? "Custom share code is active. Shared crafts are visible only to peers using the same server and code."
    : "Auto private code is active. This install stays local-only until you replace it with your own share code.";

  if (!syncState) {
    return `${passwordNote} Share settings are not loaded yet.`;
  }
  if (syncState.lastError) {
    return `${passwordNote} Last sync error: ${syncState.lastError}`;
  }
  if (syncState.running && syncState.connection === "connected") {
    return `${passwordNote} Live sync is active with ${formatCount(syncState.transportPeerCount || 0)} live links and ${formatCount(syncState.remotePeerCount || 0)} visible peers.`;
  }
  if (syncState.running) {
    return `${passwordNote} Sync is running and waiting for peers. Right now there are ${formatCount(syncState.remotePeerCount || 0)} visible peers.`;
  }
  if (syncState.lastSyncedAt) {
    return `${passwordNote} Remote cache last refreshed ${formatTimestamp(syncState.lastSyncedAt)}. ${formatCount(syncState.publishedCount || localCrafts.filter((craft) => craft?.sharing?.enabled === true).length)} crafts are currently shared out.`;
  }
  return `${passwordNote} No sync has run yet.`;
}

