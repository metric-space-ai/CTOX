function normalizeLegacyCraftTab(value) {
  const candidate = String(value || "").trim().toLowerCase();
  if (["tools", "browser-functions", "browser-capabilities", "capabilities"].includes(candidate)) {
    return "browser-functions";
  }
  if (["tool-scripts", "scripts"].includes(candidate)) return "tool-scripts";
  if (candidate === "skillset") return "skills";
  if (candidate === "model") return "model";
  if (candidate === "lineage") return "lineage";
  return "model";
}

function buildCraftPageUrl(craftId, tab = "model") {
  const baseUrl = globalThis.chrome?.runtime?.getURL
    ? globalThis.chrome.runtime.getURL("craft.html")
    : new URL("craft.html", globalThis.location.href).toString();
  const url = new URL(baseUrl);
  const normalizedCraftId = String(craftId || "").trim();
  const normalizedTab = String(tab || "model").trim() || "model";
  if (normalizedCraftId) {
    url.searchParams.set("craft", normalizedCraftId);
  }
  url.searchParams.set("tab", normalizedTab);
  url.hash = normalizedTab;
  return url.toString();
}

function readPageRoute() {
  try {
    const url = new URL(location.href);
    return {
      section:
      normalizeSectionId(url.searchParams.get("section")) ||
      normalizeSectionId(String(location.hash || "").replace(/^#/, "").split("/")[0]) ||
      "general",
    };
  } catch (_error) {
    return {
      section: "general",
    };
  }
}

function readRequestedSection() {
  return readPageRoute().section;
}

function writePageRoute(route = {}) {
  const url = new URL(location.href);
  const sectionId = normalizeSectionId(route.section) || "general";

  url.searchParams.set("section", sectionId);
  url.searchParams.delete("craft");
  url.searchParams.delete("bundleTab");
  url.hash = sectionId;
  history.replaceState(null, "", url.toString());
}

function normalizeSyncSettings(value) {
  const raw = value && typeof value === "object" ? value : {};
  return {
    ...DEFAULT_SYNC_SETTINGS,
    ...raw,
    ...normalizeRemoteComputeSettings(raw),
  };
}

async function ensureRemoteComputeOffscreen(settings = DEFAULT_SYNC_SETTINGS, reason = "options") {
  if (!REMOTE_COMPUTE_ROLLOUT_ENABLED) return null;
  const normalized = normalizeSyncSettings(settings);
  const shouldBootstrap =
    normalized.mode === "continuous" &&
    (normalized.computeOfferEnabled === true || normalized.remoteExecutionEnabled === true);
  if (!shouldBootstrap || !chrome?.runtime?.sendMessage) return null;
  return await new Promise((resolve) => {
    chrome.runtime.sendMessage(
      {
        type: "compute:ensure-offscreen",
        reason,
      },
      (response) => {
        resolve(response || null);
      },
    );
  });
}

function formatCount(value) {
  return new Intl.NumberFormat(undefined, { maximumFractionDigits: 0 }).format(
    Math.max(0, Number(value) || 0),
  );
}

function formatTimestamp(value) {
  const text = String(value || "").trim();
  if (!text) return "—";
  const timestamp = new Date(text);
  if (Number.isNaN(timestamp.getTime())) return text;
  return new Intl.DateTimeFormat(undefined, {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(timestamp);
}

