function ModelsSection({ configVersion, onEditProvider }) {
  const [providers, setProviders] = React.useState({});
  const [slots, setSlots] = React.useState(configApi.createDefaultModelSlots());
  const [loading, setLoading] = React.useState(true);
  const [error, setError] = React.useState("");

  const loadState = React.useCallback(async () => {
    setLoading(true);
    setError("");
    try {
      const nextProviders = await configApi.readProviders();
      const nextSlots = await configApi.readModelSlots(nextProviders);
      setProviders(nextProviders);
      setSlots(nextSlots);
    } catch (loadError) {
      setError(loadError instanceof Error ? loadError.message : String(loadError || "Models could not be loaded."));
    } finally {
      setLoading(false);
    }
  }, []);

  React.useEffect(() => {
    void loadState();
  }, [loadState, configVersion]);

  const updateSlot = React.useCallback(
    async (slotId, patch) => {
      const nextSlots = configApi.patchModelSlot(slots, slotId, patch, providers);
      setSlots(nextSlots);
      setError("");
      try {
        const writtenSlots = await configApi.writeModelSlots(nextSlots, providers);
        setSlots(writtenSlots);
      } catch (saveError) {
        setError(saveError instanceof Error ? saveError.message : String(saveError || "Models could not be saved."));
      }
    },
    [providers, slots],
  );

  const setup = React.useMemo(
    () => configApi.getRequiredSetupStatus(slots, providers),
    [slots, providers],
  );

  return h("section", { className: "options-card" }, [
    h("div", { className: "section-header", key: "header" }, [
      h("div", { key: "copy" }, [
        h("div", { className: "section-eyebrow", key: "eyebrow" }, "Model roles"),
        h("h1", { className: "section-title", key: "title" }, "Models"),
        h(
          "p",
          { className: "section-description", key: "description" },
          "Choose the provider and model for each role. Provider editing stays one click away instead of being hidden behind the startup flow.",
        ),
      ]),
    ]),
    loading
      ? h("div", { className: "status-message", key: "loading" }, "Loading model roles...")
      : null,
    !loading && setup.ready
      ? h("div", { className: "setup-banner setup-banner-ready", key: "ready" }, [
          h("strong", { key: "title" }, "Required startup roles are configured."),
          h("span", { key: "body" }, setup.items.map((item) => item.label).join(" · ")),
        ])
      : null,
    !loading && !setup.ready
      ? h("div", { className: "setup-banner", key: "missing" }, [
          h("strong", { key: "title" }, "Agent, Batch, and Vision still need valid providers/models."),
          h(
            "div",
            { className: "setup-banner-list", key: "list" },
            setup.missingItems.map((item) =>
              h("span", { className: "setup-banner-chip", key: item.slotId }, `${item.label}: ${item.reason}`),
            ),
          ),
        ])
      : null,
    error ? h("div", { className: "status-message status-message-error", key: "error" }, error) : null,
    h(
      "div",
      { className: "editor-list", key: "list" },
      configApi.MODEL_SLOT_DEFS.map((slotDef) =>
        h(RoleCard, {
          slotDef,
          slot: slots[slotDef.id] || {},
          providers,
          onSlotChange: (slotId, patch) => void updateSlot(slotId, patch),
          onEditProvider,
          key: slotDef.id,
        }),
      ),
    ),
  ]);
}

function Sidebar({ sections, activeId, onSelect, themeId }) {
  const activeTheme = themeApi?.getTheme?.(themeId) || null;
  return h(
    "nav",
    {
      style: {
        width: 220,
        flex: "0 0 220px",
        borderRight: "1px solid var(--line)",
        padding: 12,
        minHeight: "100vh",
      },
    },
    h("div", {}, [
      h("div", { className: "options-brand-eyebrow", key: "eyebrow" }, "Fuck API, Train Local AI"),
      h("h2", { style: { margin: "8px 0 6px", fontSize: 28 }, key: "title" }, "Settings"),
      activeTheme
        ? h(
            "div",
            { style: { color: "var(--muted)", fontSize: 13, lineHeight: 1.45, marginBottom: 10 }, key: "copy" },
            `${activeTheme.title}. ${activeTheme.shortNote || activeTheme.description}`,
          )
        : null,
      ...sections.map((section) =>
        h(
          "button",
          {
            type: "button",
            onClick: () => onSelect(section.id),
            style: {
              display: "block",
              width: "100%",
              textAlign: "left",
              margin: "6px 0",
              padding: "10px 12px",
              background: section.id === activeId ? "var(--accent)" : "var(--surface-strong)",
              color: section.id === activeId ? "#fff" : "var(--text)",
              border: "1px solid var(--line)",
              borderRadius: 6,
              fontWeight: section.id === activeId ? 700 : 500,
            },
            key: section.id,
          },
          section.label,
        ),
      ),
    ]),
  );
}

function SectionHost({
  route,
  configVersion,
  themeId,
  focusedProviderId,
  onThemeChange,
  onJumpToSection,
}) {
  const activeSection = route.section;
  if (activeSection === "general") {
    return h(GeneralSection, { themeId, onThemeChange, configVersion });
  }
  if (activeSection === "providers") {
    return h(ProvidersSection, { configVersion, focusedProviderId });
  }
  if (activeSection === "models") {
    return h(ModelsSection, {
      configVersion,
      onEditProvider: (providerId) => onJumpToSection("providers", providerId),
    });
  }
  if (activeSection === "share") {
    return h(ShareSection);
  }
  return null;
}

function OptionsApp({ initialThemeId }) {
  const [route, updateRoute] = usePageRoute();
  const [themeId, setThemeId] = React.useState(initialThemeId);
  const [configVersion, setConfigVersion] = React.useState(0);
  const [focusedProviderId, setFocusedProviderId] = React.useState("");

  const handleThemeChange = React.useCallback(async (nextThemeId) => {
    const normalizedThemeId = themeApi?.normalizeThemeId?.(nextThemeId) || nextThemeId;
    setThemeId(normalizedThemeId);
    themeApi?.applyTheme?.(normalizedThemeId);
    try {
      await themeApi?.writeTheme?.(normalizedThemeId);
    } catch (_error) {
      // keep UI responsive even when persistence fails
    }
  }, []);

  const handleJumpToSection = React.useCallback(
    (sectionId, providerId = "") => {
      if (sectionId === "providers" || sectionId === "models") {
        setConfigVersion((version) => version + 1);
      }
      setFocusedProviderId(sectionId === "providers" ? providerId : "");
      updateRoute({ section: sectionId });
    },
    [updateRoute],
  );

  return h("div", { className: "options-app" }, [
    h(Sidebar, {
      sections: SECTION_DEFS,
      activeId: route.section,
      onSelect: (sectionId) => {
        setFocusedProviderId("");
        updateRoute({ section: sectionId });
      },
      themeId,
      key: "sidebar",
    }),
    h("main", { style: { flex: 1, padding: 16, minWidth: 0 }, key: "main" }, [
      h("div", { className: "options-stack", key: "stack" }, [
        h(SectionHost, {
          route,
          configVersion,
          themeId,
          focusedProviderId,
          onThemeChange: handleThemeChange,
          onJumpToSection: handleJumpToSection,
          key: route.section,
        }),
      ]),
    ]),
  ]);
}

function redirectLegacyWorkspaceRoute() {
  try {
    const url = new URL(location.href);
    const legacySection =
      normalizeLegacyWorkspaceSection(url.searchParams.get("section")) ||
      normalizeLegacyWorkspaceSection(String(location.hash || "").replace(/^#/, "").split("/")[0]);
    const craftId = String(url.searchParams.get("craft") || "").trim();
    if (!legacySection || !craftId) {
      return false;
    }
    const targetTab =
      legacySection === "training-data"
        ? "training-data"
        : normalizeLegacyCraftTab(url.searchParams.get("bundleTab"));
    location.replace(buildCraftPageUrl(craftId, targetTab));
    return true;
  } catch (_error) {
    return false;
  }
}

async function init() {
  if (redirectLegacyWorkspaceRoute()) {
    return;
  }

  await ensureCraftSyncLoaded();

  let initialThemeId = themeApi?.DEFAULT_THEME_ID || "copyshop";
  try {
    initialThemeId = (await themeApi?.hydrateTheme?.()) || initialThemeId;
  } catch (_error) {
    themeApi?.applyTheme?.(initialThemeId);
  }

  const appContainer = document.querySelector("#app-container");
  if (!appContainer) {
    throw new Error("Can not find #app-container");
  }
  const root = clientExports.createRoot(appContainer);
  root.render(h(OptionsApp, { initialThemeId }));
}

void init();
