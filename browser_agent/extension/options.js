function ProvidersSection({ configVersion, focusedProviderId }) {
  const [providers, setProviders] = React.useState({});
  const [drafts, setDrafts] = React.useState({});
  const [loading, setLoading] = React.useState(true);
  const [savingId, setSavingId] = React.useState("");
  const [saveErrors, setSaveErrors] = React.useState({});

  const loadProviders = React.useCallback(async () => {
    setLoading(true);
    try {
      const nextProviders = await configApi.readProviders();
      const nextDrafts = {};
      for (const [providerId, provider] of Object.entries(nextProviders)) {
        nextDrafts[providerId] = providerDraftFromRecord(provider);
      }
      setProviders(nextProviders);
      setDrafts(nextDrafts);
      setSaveErrors({});
    } finally {
      setLoading(false);
    }
  }, []);

  React.useEffect(() => {
    void loadProviders();
  }, [loadProviders, configVersion]);

  const orderedProviderIds = React.useMemo(
    () => configApi.orderProviderIds(providers),
    [providers],
  );

  const saveProvider = React.useCallback(
    async (providerId) => {
      const draft = drafts[providerId];
      if (!draft) return;
      setSavingId(providerId);
      setSaveErrors((previous) => ({ ...previous, [providerId]: "" }));
      try {
        const nextProviders = {
          ...providers,
          [providerId]: providerRecordFromDraft(draft),
        };
        const writtenProviders = await configApi.writeProviders(nextProviders);
        const normalizedSlots = await configApi.readModelSlots(writtenProviders);
        await configApi.writeModelSlots(normalizedSlots, writtenProviders);
        const nextDrafts = {};
        for (const [currentProviderId, currentProvider] of Object.entries(writtenProviders)) {
          nextDrafts[currentProviderId] = providerDraftFromRecord(currentProvider);
        }
        setProviders(writtenProviders);
        setDrafts(nextDrafts);
      } catch (error) {
        setSaveErrors((previous) => ({
          ...previous,
          [providerId]: error instanceof Error ? error.message : String(error || "Provider could not be saved."),
        }));
      } finally {
        setSavingId("");
      }
    },
    [drafts, providers],
  );

  const deleteProvider = React.useCallback(
    async (providerId) => {
      setSavingId(providerId);
      setSaveErrors((previous) => ({ ...previous, [providerId]: "" }));
      try {
        const nextProviders = { ...providers };
        delete nextProviders[providerId];
        const writtenProviders = await configApi.writeProviders(nextProviders);
        const normalizedSlots = await configApi.readModelSlots(writtenProviders);
        await configApi.writeModelSlots(normalizedSlots, writtenProviders);
        const nextDrafts = {};
        for (const [currentProviderId, currentProvider] of Object.entries(writtenProviders)) {
          nextDrafts[currentProviderId] = providerDraftFromRecord(currentProvider);
        }
        setProviders(writtenProviders);
        setDrafts(nextDrafts);
      } catch (error) {
        setSaveErrors((previous) => ({
          ...previous,
          [providerId]: error instanceof Error ? error.message : String(error || "Provider could not be deleted."),
        }));
      } finally {
        setSavingId("");
      }
    },
    [providers],
  );

  const addCustomProvider = React.useCallback(() => {
    const provider = configApi.createCustomProvider(providers);
    setProviders((previous) => ({ ...previous, [provider.id]: provider }));
    setDrafts((previous) => ({ ...previous, [provider.id]: providerDraftFromRecord(provider) }));
  }, [providers]);

  return h("section", { className: "options-card" }, [
    h("div", { className: "section-header", key: "header" }, [
      h("div", { key: "copy" }, [
        h("div", { className: "section-eyebrow", key: "eyebrow" }, "Provider catalog"),
        h("h1", { className: "section-title", key: "title" }, "Providers"),
        h(
          "p",
          { className: "section-description", key: "description" },
          "Edit API keys, endpoints, and model lists here. This follows the reference sidebar-driven settings flow instead of hiding provider edits behind the startup gate.",
        ),
      ]),
      h(
        "button",
        {
          className: "options-button options-button-ghost",
          type: "button",
          onClick: addCustomProvider,
          key: "add",
        },
        "Add custom provider",
      ),
    ]),
    loading
      ? h("div", { className: "status-message", key: "loading" }, "Loading providers...")
      : h(
          "div",
          { className: "editor-list", key: "list" },
          orderedProviderIds.map((providerId) =>
            h(ProviderEditor, {
              provider: providers[providerId],
              draft: drafts[providerId] || providerDraftFromRecord(providers[providerId]),
              isFocused: focusedProviderId === providerId,
              isSaving: savingId === providerId,
              saveError: saveErrors[providerId],
              onChange: (nextDraft) => {
                setDrafts((previous) => ({
                  ...previous,
                  [providerId]: nextDraft,
                }));
              },
              onSave: () => void saveProvider(providerId),
              onDelete: () => void deleteProvider(providerId),
              key: providerId,
            }),
          ),
        ),
  ]);
}

